const theme = createTheme();

export default function SignUp() {

  const navigate = useNavigate();

  const [credentials, setCredentials] = useState({name:"",email:"",password:"",cpassword:""});

  const onChange = (e) => {
    setCredentials({ ...credentials, [e.target.name]: e.target.value });
}

  const handleSubmit = async (event) => {
    event.preventDefault();
    console.log(process.env.REACT_APP_SERVER_DOMAIN);

    if(credentials.cpassword==credentials.password){

    
    
    const response = await fetch(`${process.env.REACT_APP_SERVER_DOMAIN}/auth/createUser`, {
      method: 'POST',

      headers: {
          'Content-Type': 'application/json',
      },
      body: JSON.stringify({ name: credentials.name, email: credentials.email, password: credentials.password })

  });

  const json = await response.json();
  console.log(json);

  if (json.success) {   //checking if we successfully got the response or not
      //saving the auth token and redirecting

      localStorage.setItem("token", json.token);  //saving the auth token that we got on logging in the localstorage so that it can be used to fetch the notes of the logged in user

      navigate('/');   //redirecting to the home page


  }
  else {
      alert("Error");
  }

  }

  else{
    alert("Confirm password must be same as password")
  }

  };

  return (

    <ThemeProvider theme={theme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            {/* <LockOutlinedIcon /> */}
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign up
          </Typography>
          <Box component="form" noValidate onSubmit={handleSubmit} sx={{ mt: 3 }}>
            <Grid container spacing={2}>
              <Grid item xs={12}>
                <TextField onChange={onChange}
                  autoComplete="given-name"
                  name="name"
                  required
                  fullWidth
                  id="name"
                  label="Name"
                  autoFocus
                />
              </Grid>
              <Grid item xs={12}>
                <TextField onChange={onChange}
                  required
                  fullWidth
                  id="email"
                  label="Email Address"
                  name="email"
                  autoComplete="email"
                />
              </Grid>
              <Grid item xs={12}>
                <TextField onChange={onChange}
                  required
                  fullWidth
                  name="password"
                  label="Password"
                  type="password"
                  id="password"
                  autoComplete="new-password"
                />
              </Grid>

              <Grid item xs={12}>
                <TextField onChange={onChange}
                  required
                  fullWidth
                  name="cpassword"
                  label="Confirm Password"
                  type="password"
                  id="cpassword"
                  autoComplete="new-password"
                />
              </Grid>
              
            </Grid>
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign Up
            </Button>
            <Grid container justifyContent="flex-end">
              <Grid item>
                <NavLink to="/login" variant="body2">
                  Already have an account? Sign in
                </NavLink>
              </Grid>
            </Grid>
          </Box>
        </Box>
        {/* <Copyright sx={{ mt: 5 }} /> */}
      </Container>
    </ThemeProvider>
  );
}